

public class Page {
    Integer id;
    String name;
    Integer year;
    String color;
    String pantone_value;

    public Page(Integer id, String name, Integer year, String color, String pantone_value) {
        this.id = id;
        this.name = name;
        this.year = year;
        this.color = color;
        this.pantone_value = pantone_value;
    }
}

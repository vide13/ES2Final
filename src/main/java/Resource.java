public class Resource {
    Integer id;
    String name;
    String year;
    String color;
    String pantone_value;

    public Resource(Integer id, String name, String year, String color, String pantone_value) {
        this.id = id;
        this.name = name;
        this.year = year;
        this.color = color;
        this.pantone_value = pantone_value;
    }


}
